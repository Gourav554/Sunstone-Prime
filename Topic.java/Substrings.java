import java.util.*;
public class Substrings {
    public static void main(String[] args) {
        
        String Sentence = "my name is yoyo"; 
        String name = Sentence.substring(11, Sentence.length());    // Start,end Index      //koi bhi String mmai se sab string nikalne ke liye just a word        
        System.out.println(name);          
    }
}